const Element& Element::getElement(string symbol, int A)
{
    const map<string,vector<Element>>& es = elements();

    for (char& c : symbol) c = tolower(c);
    symbol[0] = toupper(symbol[0]);
    auto i = es.find(symbol);

    if (i != es.end())
    {
        for (const Element& e : i->second)
        {
            if (e.A == A) return e;
        }
        throw runtime_error(strprintf("Isotope A=%d not found for element %s", A, symbol.c_str()));
    }
    throw runtime_error(strprintf("Element %s not found", symbol.c_str()));
}

const Element& Element::getElement(string symbol)
{
    const map<string,vector<Element>>& es = elements();

    for (char& c : symbol) c = tolower(c);
    symbol[0] = toupper(symbol[0]);
    auto i = es.find(symbol);

    if (i != es.end())
    {
        return i->second[0];
    }
    throw runtime_error(strprintf("Element %s not found", symbol.c_str()));
}

